package com.example.myapplication;

public class Party {
    String name;
    String adres;
    String data;
    String time;
    String dist;
    String details;
    String image;
    String imageURL;

    public Party (String name, String adres, String data, String time, String dist, String image, String details, String imageURL) {
        this.name = name;
        this.adres = adres;
        this.data = data;
        this.time = time;
        this.dist = dist;
        this.details = details;
        this.image = image;
        this.imageURL = imageURL;

    }
}
